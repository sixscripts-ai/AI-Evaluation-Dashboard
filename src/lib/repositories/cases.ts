import { prisma, generateId } from '../db.js';
import { EvalCase, EvidenceSource } from '../../types.js';

export async function getCasesBySuiteId(suiteId: string) {
  return await prisma.evalCase.findMany({
    where: { suiteId }
  });
}

export async function getActiveCasesBySuiteId(suiteId: string) {
  return await prisma.evalCase.findMany({
    where: { suiteId, isActive: true }
  });
}

export async function getCaseById(id: string) {
  return await prisma.evalCase.findUnique({
    where: { id }
  });
}

export async function getAllCases() {
  return await prisma.evalCase.findMany();
}

export async function createCaseWithSource(testCase: Omit<EvalCase, 'id' | 'createdAt' | 'updatedAt'>) {
  const caseId = generateId('case');
  const sourceId = generateId('source');

  // We use a transaction to create both case and default source
  const [newCase, newSource] = await prisma.$transaction([
    prisma.evalCase.create({
      data: {
        ...testCase,
        id: caseId,
        tags: testCase.tags as any,
        assertions: testCase.assertions as any,
      }
    }),
    prisma.evidenceSource.create({
      data: {
        id: sourceId,
        caseId: caseId,
        title: `Reference Guideline Source: ${testCase.name}`,
        sourceType: 'document',
        excerpt: testCase.requiredEvidence,
        metadata: { autoGenerated: true } as any
      }
    })
  ]);

  return { newCase, newSource };
}

export async function updateCase(id: string, updateData: Partial<EvalCase>) {
  return await prisma.evalCase.update({
    where: { id },
    data: {
      ...updateData,
      tags: updateData.tags as any,
      assertions: updateData.assertions as any,
    }
  });
}
