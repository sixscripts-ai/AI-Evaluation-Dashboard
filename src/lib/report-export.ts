import { RunSummary, CaseComparisonRow, Disagreement, DiagnosisPanel } from './comparisons.js';

export function generateMarkdownReport(
  suiteName: string,
  runs: RunSummary[],
  rows: CaseComparisonRow[],
  disagreements: Disagreement[],
  diagnosis: DiagnosisPanel
): string {
  let md = `# EvalBench Provider Comparison Report: ${suiteName}\n\n`;
  md += `*Generated on: ${new Date().toLocaleString()}*\n\n`;

  // 1. Selected Runs Summary Table
  md += `## 📊 Selected Evaluation Runs\n\n`;
  md += `| Provider | Model | Mode | Avg Score | Pass / Partial / Fail | Avg Latency | Tokens | Est. Cost | Run ID |\n`;
  md += `| :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- | :--- |\n`;
  runs.forEach(r => {
    const costStr = typeof r.estimatedCost === 'number' ? `$${r.estimatedCost.toFixed(5)}` : r.estimatedCost;
    md += `| **${r.provider.toUpperCase()}** | \`${r.modelName}\` | ${r.runMode} | **${Math.round(r.averageScore)}%** | ${r.passCount} / ${r.partialCount} / ${r.failCount} | ${r.averageLatencyMs}ms | ${r.totalTokens} | ${costStr} | \`${r.runId}\` |\n`;
  });
  md += `\n`;

  // 2. Diagnosis Panel
  md += `## 🏆 Winner & Diagnostics Summary\n\n`;
  if (diagnosis.bestScoringRun) {
    md += `- **Best Scoring Model:** **${diagnosis.bestScoringRun.modelName}** (${Math.round(diagnosis.bestScoringRun.averageScore)}% average score)\n`;
  }
  if (diagnosis.fastestRun) {
    md += `- **Fastest Response Model:** **${diagnosis.fastestRun.modelName}** (${diagnosis.fastestRun.averageLatencyMs}ms average latency)\n`;
  }
  if (diagnosis.mostReliableRun) {
    md += `- **Most Reliable Model:** **${diagnosis.mostReliableRun.modelName}** (${diagnosis.mostReliableRun.failCount} failures)\n`;
  }
  if (diagnosis.mostConsistentRun) {
    md += `- **Most Consistent Model:** **${diagnosis.mostConsistentRun.modelName}**\n`;
  }
  if (diagnosis.mostFailuresRun && diagnosis.mostFailuresRun.failCount > 0) {
    md += `- **Highest Failure Model:** **${diagnosis.mostFailuresRun.modelName}** (${diagnosis.mostFailuresRun.failCount} failures)\n`;
  }
  md += `- **Cases Flagged for Review:** **${diagnosis.casesNeedingReviewCount}** medium/high disagreement case(s) detected\n\n`;

  // 3. Disagreement Detection
  md += `## ⚠️ Disagreement & Outlier Warnings\n\n`;
  if (disagreements.length === 0) {
    md += `*No significant model disagreements detected across all test cases. Providers are aligned!*\n\n`;
  } else {
    md += `The system detected **${disagreements.length}** case(s) where models deviated significantly:\n\n`;
    disagreements.forEach((d, idx) => {
      const sevEmoji = d.severity === 'high' ? '🔴 [HIGH]' : d.severity === 'medium' ? '🟡 [MEDIUM]' : '🟢 [LOW]';
      md += `### ${idx + 1}. ${sevEmoji} ${d.caseName}\n`;
      md += `- **Type:** \`${d.type.replace('_', ' ').toUpperCase()}\`\n`;
      md += `- **Models involved:** ${d.modelsInvolved.join(', ')}\n`;
      md += `- **Explanation:** ${d.explanation}\n\n`;
    });
  }

  // 4. Case-by-Case Comparison Table
  md += `## 📋 Case-by-Case Breakdown\n\n`;
  
  // Headers
  md += `| Test Case |`;
  runs.forEach(r => {
    md += ` ${r.modelName} (${r.provider}) |`;
  });
  md += `\n| :--- |`;
  runs.forEach(() => {
    md += ` :--- |`;
  });
  md += `\n`;

  // Rows
  rows.forEach(row => {
    md += `| **${row.caseName}** |`;
    runs.forEach(r => {
      const res = row.resultsByRun[r.runId];
      if (res) {
        const outSnippet = res.actualOutput 
          ? res.actualOutput.substring(0, 50).replace(/\n/g, ' ') + (res.actualOutput.length > 50 ? '...' : '')
          : '*No output*';
        const badge = res.status.toUpperCase();
        md += ` **${badge}** (${res.score}%)<br/>⏱️ ${res.latencyMs}ms<br/>🔍 *"${outSnippet}"* |`;
      } else {
        md += ` *N/A (Not Run)* |`;
      }
    });
    md += `\n`;
  });
  md += `\n---\n*Report generated by EvalBench AI Evaluation Console.*`;

  return md;
}

export function generateJSONReport(
  suiteName: string,
  runs: RunSummary[],
  rows: CaseComparisonRow[],
  disagreements: Disagreement[],
  diagnosis: DiagnosisPanel
): string {
  const data = {
    reportType: 'EvalBench Provider Comparison Report',
    suiteName,
    generatedAt: new Date().toISOString(),
    runsSummaries: runs,
    winnerDiagnostics: {
      bestScoringModel: diagnosis.bestScoringRun ? {
        runId: diagnosis.bestScoringRun.runId,
        modelName: diagnosis.bestScoringRun.modelName,
        averageScore: diagnosis.bestScoringRun.averageScore
      } : null,
      fastestModel: diagnosis.fastestRun ? {
        runId: diagnosis.fastestRun.runId,
        modelName: diagnosis.fastestRun.modelName,
        averageLatencyMs: diagnosis.fastestRun.averageLatencyMs
      } : null,
      mostReliableModel: diagnosis.mostReliableRun ? {
        runId: diagnosis.mostReliableRun.runId,
        modelName: diagnosis.mostReliableRun.modelName,
        failCount: diagnosis.mostReliableRun.failCount
      } : null,
      casesNeedingReviewCount: diagnosis.casesNeedingReviewCount
    },
    disagreements,
    caseByCaseData: rows.map(r => ({
      caseId: r.caseId,
      caseName: r.caseName,
      input: r.input,
      expectedOutput: r.expectedOutput,
      requiredEvidence: r.requiredEvidence,
      results: Object.keys(r.resultsByRun).map(runId => {
        const res = r.resultsByRun[runId];
        const runInfo = runs.find(s => s.runId === runId);
        return {
          runId,
          modelName: runInfo?.modelName || 'unknown',
          provider: runInfo?.provider || 'unknown',
          result: res ? {
            status: res.status,
            score: res.score,
            latencyMs: res.latencyMs,
            evidenceMatched: res.evidenceMatched,
            actualOutput: res.actualOutput,
            providerError: res.providerError
          } : null
        };
      })
    }))
  };

  return JSON.stringify(data, null, 2);
}
